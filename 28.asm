CSEG SEGMENT
	ASSUME CS:CSEG, DS: DSEG
	START:
		MOV		AX, DSEG
		MOV 	DS, AX
		LEA 	SI, DAT
		MOV 	AL, [SI]
		CMP 	AL, [SI+1]
		JB 		CMP1
		XCHG 	AL, [SI+1]
		MOV 	[SI], AL
	CMP1:
		MOV 	AL, [SI+1]
		CMP 	AL, [SI+2]
		JA 		DISP
		MOV 	BUF, AL
		MOV 	AH, 4CH
		INT 	21H
	DISP:
		MOV 	AL, [SI+2]
		MOV 	BUF, AL
		MOV 	AH, 4CH
		INT 	21H
CSEG ENDS
END START