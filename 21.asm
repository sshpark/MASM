DSEG SEGMENT
	MESS	DB	"ERROR", 0DH, 0AH, "$"
DSEG ENDS

CSEG SEGMENT
	ASSUME CS: CSEG, DS: DSEG
	START:
		MOV		AX, DSEG
		MOV		DS, AX

		MOV		BX, 0000H

		CALL	INPUT
		CALL	COMPARE
		CMP 	BX, 0000H
		JE 		AGAIN
		SUB 	AL, 20H
		MOV 	DL, AL
		CALL 	DISP
		JMP 	OVER1

	ERR:
		MOV 	DX, OFFSET MESS
		MOV 	AH, 09H
		INT 	21H
		JMP 	OVER1

	AGAIN:
		ADD 	AL, 20H
		MOV 	DL, AL
		CALL 	DISP

	OVER1:
		MOV 	AH, 4CH
		INT 	21H

	INPUT PROC NEAR
		MOV		AH, 01H
		INT 	21H
		RET
	INPUT ENDP

	COMPARE PROC NEAR
		CMP 	AL, 'A'
		JB		NOMATCH
		CMP 	AL, 'Z'
		JA 		XIAO
		MOV 	BX, 0001H
		JMP 	EXIT1

	NOMATCH:
		MOV 	BX, 0000H
		JMP 	EXIT1

	XIAO:
		CMP 	AL, 'a'
		JB 		NOMATCH
		CMP 	AL, 'z'
		JA 		NOMATCH
		MOV 	BX, 0002H

	EXIT1:
		RET
		COMPARE ENDP

	DISP PROC NEAR
		MOV		AH, 02H
		INT 	21H
		RET
	DISP ENDP

CSEG ENDS
END START

	